package designer.bean;

public class Comp {

	private int f_id;
	private String f_key;
	private int f_type;
	private int f_layout;
	private String f_config_key;
	private String f_config_default;
	private boolean f_allow_children;

	public int getF_id() {
		return f_id;
	}

	public void setF_id(int f_id) {
		this.f_id = f_id;
	}

	public String getF_key() {
		return f_key;
	}

	public void setF_key(String f_key) {
		this.f_key = f_key;
	}

	public int getF_type() {
		return f_type;
	}

	public void setF_type(int f_type) {
		this.f_type = f_type;
	}

	public int getF_layout() {
		return f_layout;
	}

	public void setF_layout(int f_layout) {
		this.f_layout = f_layout;
	}

	public String getF_config_key() {
		return f_config_key;
	}

	public void setF_config_key(String f_config_key) {
		this.f_config_key = f_config_key;
	}

	public String getF_config_default() {
		return f_config_default;
	}

	public void setF_config_default(String f_config_default) {
		this.f_config_default = f_config_default;
	}

	public boolean isF_allow_children() {
		return f_allow_children;
	}

	public void setF_allow_children(boolean f_allow_children) {
		this.f_allow_children = f_allow_children;
	}

}
