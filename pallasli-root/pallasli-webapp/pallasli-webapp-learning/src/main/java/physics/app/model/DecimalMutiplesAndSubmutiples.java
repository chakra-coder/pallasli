package physics.app.model;

public class DecimalMutiplesAndSubmutiples {
	private int id;
	private double submutiple;
	private String prefix;
	private String symbol;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public double getSubmutiple() {
		return submutiple;
	}

	public void setSubmutiple(double submutiple) {
		this.submutiple = submutiple;
	}

	public String getPrefix() {
		return prefix;
	}

	public void setPrefix(String prefix) {
		this.prefix = prefix;
	}

	public String getSymbol() {
		return symbol;
	}

	public void setSymbol(String symbol) {
		this.symbol = symbol;
	}

}